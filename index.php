<?php
    $stringy = "This is a string";
    $inty = 1;
    $floaty = 1.5;
    $bigfoot = true;

    echo $stringy . "<br>";
    echo $inty . "<br>";
    echo $floaty . "<br>";
    echo $bigfoot . "<br>";


    // Create two integers and one float
    $int1 = 10;
    $int2 = 3;
    $float1 = 2.5;

    // Addition
    echo "Addition: " . ($int1 + $int2 + $float1) . "<br>";

    // Subtraction
    echo "Subtraction: " . ($int1 - $int2 - $float1) . "<br>";

    // Multiplication
    echo "Multiplication: " . ($int1 * $int2 * $float1) . "<br>";

    // Division
    echo "Division: " . ($int1 / $int2 / $float1) . "<br>";

    // Modulus (only works with integers)
    echo "Modulus: " . ($int1 % $int2) . "<br>";

    
